const input = document.querySelector("#code-input")
const btn = document.querySelector("#btn")

btn.onclick = (e) => {
    if (input.value.length !== 6 && parseInt(input.value) !== NaN) return
    window.location.href = `/game?gameId=${input.value}`
}



